uname -a 

# uname → Muestra información del sistema operativo y kernel.
# -a → Opción para mostrar todos los detalles: nombre del kernel, versión, arquitectura, etc.

lscpu

# ls → Lista información (en este caso, no de archivos sino del CPU).
# cpu → Indica que se debe mostrar detalles del procesador: núcleos, arquitectura, velocidad.

free -h

# free → Muestra el uso de memoria RAM y swap.
# -h → Formato “humano”, convierte bytes en KB, MB, GB para fácil lectura.

df -h

# df → Muestra el uso del espacio en disco por sistema de archivos.
# -h → Formato “humano”, muestra tamaños en KB, MB, GB.